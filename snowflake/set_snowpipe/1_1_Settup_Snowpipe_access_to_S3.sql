Step 1: Configure access permissions for the S3 bucket

AWS access control requirements
Snowflake requires the following permissions on an S3 bucket and folder to be able to access files in the folder (and sub-folders):

- s3:GetBucketLocation
- s3:GetObject
- s3:GetObjectVersion
- s3:ListBucket

Note
The following additional permissions are required to perform additional SQL actions:

- Permission       SQL Action

  s3:PutObject     Unload files to the bucket
  s3:DeleteObject  Either automatically purge files from the stage after a successful load 
                   or execute REMOVE statements to remove files manually.

As a best practice, Snowflake recommends creating an IAM policy for Snowflake access to the S3 bucket. 
You can then attach the policy to the role and use the security credentials generated by AWS for the role to access files in the bucket.

POLICY (attach to) --> ROLE (use) SECURITY CREDENTIALS generated by AWS for the role to access files in the bucket.


REF: https://docs.snowflake.com/en/user-guide/data-load-snowpipe-auto-s3
