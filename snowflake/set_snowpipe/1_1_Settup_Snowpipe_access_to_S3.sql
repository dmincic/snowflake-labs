#### 3.1. snowflake - integration object ###

CREATE OR REPLACE  STORAGE INTEGRATION  AWS_STORAGE_INTEGRATION
  TYPE = EXTERNAL_STAGE
  ENABLED = TRUE
  STORAGE_ALLOWED_LOCATIONS = ('s3:// .../') -- THIS IS THE LIST 
  COMMENT = 'Dean, S3 storage integration'
  -- specific to AWS S3
  STORAGE_PROVIDER = 'S3'
  STORAGE_AWS_ROLE_ARN = '....role/DeansSnowflakeRole'
  STORAGE_AWS_EXTERNAL_ID = '...'
  
  ---(optional - SF auto-generate if not explicitly provided) STORAGE_AWS_EXTERNAL_ID = '...'
  -- This is an external ID we created and needs to be referenced in the thrust policy in the IAM role.
  -- REF: https://docs.snowflake.com/en/sql-reference/sql/create-storage-integration#label-create-storage-integration-aws-ext-id

### 3.2 snowflake - get SF IAM user
DESCRIBE STORAGE INTEGRATION AWS_STORAGE_INTEGRATION;

STORAGE_AWS_IAM_USER_ARN :  ...
STORAGE_AWS_ROLE_ARN     :  ...role/DeansSnowflakeRole  (ARN of the AWS ROLE defined above)
STORAGE_AWS_EXTERNAL_ID  :  ...

STORAGE_AWS_IAM_USER_ARN
    The AWS IAM user created for your Snowflake account; 
    Snowflake provisions a single IAM user for your entire Snowflake account. 
    All S3 storage integrations in your account use that IAM user.

STORAGE_AWS_EXTERNAL_ID
    The external ID that Snowflake uses to establish a trust relationship with AWS. 
    If you didn’t specify an external ID (STORAGE_AWS_EXTERNAL_ID) when you created the storage integration, 
    Snowflake generates an ID for you to use.



